package org.openpatch.scratch.extensions.text;

/**
 * The {@code TextStyle} enum represents different styles of text that can be used.
 *
 * <ul>
 *   <li>{@link #BOX} - Represents text enclosed in a box.
 *   <li>{@link #SPEAK} - Represents text in a speech bubble.
 *   <li>{@link #THINK} - Represents text in a thought bubble.
 *   <li>{@link #PLAIN} - Represents plain text without any special styling.
 * </ul>
 */
public enum TextStyle {
  BOX,
  SPEAK,
  THINK,
  PLAIN
}
