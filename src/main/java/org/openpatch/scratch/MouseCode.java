package org.openpatch.scratch;

/**
 * The {@code MouseCode} enum represents the different mouse buttons that can be used. It provides
 * three constants:
 *
 * <ul>
 *   <li>{@link #LEFT} - Represents the left mouse button.
 *   <li>{@link #RIGHT} - Represents the right mouse button.
 *   <li>{@link #CENTER} - Represents the center (or middle) mouse button.
 * </ul>
 */
public enum MouseCode {
  /** Represents the left mouse button. */
  LEFT,
  /** Represents the right mouse button. */
  RIGHT,
  /** Represents the center (or middle) mouse button. */
  CENTER
}
